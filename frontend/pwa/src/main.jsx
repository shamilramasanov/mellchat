// Removed WebSocketProvider: NewUI –Ω–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç –≥–ª–æ–±–∞–ª—å–Ω—ã–π WS –ø—Ä–æ–≤–∞–π–¥–µ—Ä
import './styles/globals.css';
